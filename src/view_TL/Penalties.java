    private void sendMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendMouseClicked
        if(!penalty.getText().isEmpty() && penaltiesTable.getSelectedRowCount() == 1) {
            type.users.setPenalty(Integer.valueOf(penalty.getText()));
            type.update("employee", 3, penaltiesTable);
            show.DataInTable("employee", penaltiesTable, 12);
        
        } else
            JOptionPane.showMessageDialog(null, "Please Complete Information and Select one row!");
    }//GEN-LAST:event_sendMouseClicked

